function unknown($id)
{
  global $olyData;
  if (!isset($olyData['mapData'][$id]))
  {
    $olyData['mapData'][$id]['seen'] = 0;
    $olyData['mapData'][$id]['terrain'] = 'unknown';
  }
}

function unknowns()
{
  global $unknowns;
  foreach ($unknowns as $k => $v)
  {
    $s = substr($k, 2);
    $p = substr($k, 0, 2);
    for ($i = $s; $i <= $v; $i++)
      unknown(sprintf("%s%02d", $p, $i));
  }
}

